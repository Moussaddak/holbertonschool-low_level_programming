/**
 *op_add - add a + b
 *@a: input
 *@b: input
 *Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - sub a - b
 *@a: input
 *@b: input
 *Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - mul a x b
 *@a: input
 *@b: input
 *Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - div a / b
 *@a: input
 *@b: input
 *Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - mod a % b
 *@a: input
 *@b: input
 *Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
